pub fn new() -> VulnFixedCtrNonce {
    rng::gen()
}

#[derive(Copy, Clone)]
pub struct VulnFixedCtrNonce {
    key: aes::Key128,
}

impl VulnFixedCtrNonce {
    pub fn ciphers(&self) -> [Vec<u8>; 40] {
        PLAINTEXTS
            .iter()
            .map(|plain| plain.b64_decode().xor_bytewise(self.stream()).collect())
            .collect::<Vec<_>>()
            .try_into()
            .unwrap()
    }

    // Case insensitive check to assert we recovered a portion of the plaintext.
    pub fn check_prefix(&self, index: usize, prefix: &str) -> bool {
        let expected: String = PLAINTEXTS[index]
            .b64_decode()
            .map(|b| b.to_ascii_lowercase() as char)
            .collect();
        expected.starts_with(&prefix.to_ascii_lowercase())
    }

    fn stream(&self) -> impl Iterator<Item = u8> {
        aes::ctr(self.key, 0)
    }
}
